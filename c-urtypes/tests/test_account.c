int main(void) {
    printf("Testing output_descriptor_from_cbor_account()...\n");
    printf("CBOR length: %zu bytes\n", account_cbor_len);
    printf("First few bytes: %02x %02x %02x %02x\n\n",
           account_cbor[0], account_cbor[1], account_cbor[2], account_cbor[3]);

    // Extract first output descriptor from Account
    char *descriptor = output_descriptor_from_cbor_account(account_cbor, account_cbor_len);

    if (!descriptor) {
        printf("FAILED: Could not extract output descriptor from Account CBOR\n");
        printf("This could be due to:\n");
        printf("  - CBOR decoding failure\n");
        printf("  - Tag mismatch (expected 311)\n");
        printf("  - Missing map key 2\n");
        printf("  - Empty output array\n");
        printf("  - Output parsing failure\n");
        return 1;
    }

    printf("First output descriptor:\n%s\n\n", descriptor);

    // Expected descriptor should be a pkh() descriptor with the HDKey
    // The Python test shows the first output is pkh with key 03eb3e2863...
    if (strstr(descriptor, "pkh(") == NULL) {
        printf("FAILED: Expected descriptor to start with 'pkh('\n");
        free(descriptor);
        return 1;
    }

    if (strstr(descriptor, "#") == NULL) {
        printf("FAILED: Expected descriptor to include checksum\n");
        free(descriptor);
        return 1;
    }

    printf("SUCCESS: Output descriptor extracted correctly from Account CBOR\n");

    free(descriptor);
    return 0;
}
